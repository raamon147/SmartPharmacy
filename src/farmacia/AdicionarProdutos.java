    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        int codigo = Integer.parseInt(txtcodigo.getText());
        String produto = txtproduto.getText();
        int categoria = jcombocateg.getSelectedIndex()+1;
        float preco = Float.parseFloat(txtpreco.getText());
        String status;
        if(jRadioButton1.isSelected()){
            status= jRadioButton1.getText();
        }else{
            status = jRadioButton2.getText();
        }
        dao.inserir(codigo, produto, preco, categoria,status);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Verifique os campos!");
        }
        txtproduto.setText("");
        txtpreco.setText("");
        txtcodigo.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

